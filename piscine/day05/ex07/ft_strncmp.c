int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int i;
	int j;
	int k;

	i = 0;
	j = 0;
	k = 0;
	while (k < n && s1[i] != '\0' && s2[j] != '\0')
	{
		i++;
		k++;
		j++;
	}
	return (*s1 - *s2);
}
