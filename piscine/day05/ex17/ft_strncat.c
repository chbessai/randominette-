char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;
	int j;

	i = 0;
	j = 0;
	k = 0;
	while (dest[i])
	{
		i++;
	}
	while (src[j] < k < nb)
	{
		i++;
		j++;
		k++;
	}
	dest[i] = '\0';
	return (dest);
}
