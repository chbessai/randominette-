void	ft_div_mod(int a, int b, int *div, int *mod)
{
	a / b = *div;
	a % b = *mod;
}

int main(void)
{
	int a = 5;
	int b = 3;
	printf("%d", ft_div_mod(a, b, *div, *mod));
	return(0);
}
